    public class ErrorHandlingMiddleware
    {
        private readonly ILog log = LogManager.GetLogger(Startup.Repository.Name, typeof(ErrorHandlingMiddleware));

        private readonly RequestDelegate next;

        public ErrorHandlingMiddleware(RequestDelegate next)
        {
            this.next = next;
        }

        public async Task Invoke(HttpContext context)
        {
            try
            {
                await next(context);
            }
            catch (Exception ex)
            {
                var statusCode = context.Response.StatusCode;
                if (ex is ArgumentException)
                {
                    statusCode = 200;
                }
                //拦截一切异常，全局处理
                if (ex.InnerException == null)
                {
                    await HandleExceptionAsync(context, statusCode, " 运行时错误 ----> " + ex.Message);
                }
                else
                {
                    await HandleExceptionAsync(context, statusCode, " 运行时错误 ----> " + ex.InnerException.Message);
                }
            }
        }

        private static Task HandleExceptionAsync(HttpContext context, int statusCode, string msg)
        {
            var data = new { code = statusCode.ToString(), is_success = false, msg = msg };
            var result = JsonConvert.SerializeObject(new { data = data });
            context.Response.ContentType = "application/json;charset=utf-8";
            return context.Response.WriteAsync(result);
        }
    }

    public static class ErrorHandlingExtensions
    {
        public static IApplicationBuilder UseErrorHandling(this IApplicationBuilder builder)
        {
            return builder.UseMiddleware<ErrorHandlingMiddleware>();
        }

    }
}
